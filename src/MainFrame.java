    private DebitCard createDebitCard(long musteriId, short branchCode, String cardNumber, String style, String uniqId, Date expiryDate) {
        DebitCard card = new DebitCard();
        card.setCardBranchCode(branchCode);
        card.setCardEmboss(CompanyDbMenagement.GetCustomerName(musteriId));
        card.setCardNumber(cardNumber);
        card.setCardStyleCode(style);
        card.setCardUniqId(uniqId);
        card.setCustomerId(musteriId);
        card.setExpiryDate(expiryDate);
        card.setInsertDate(Timestamp.valueOf(LocalDateTime.now()));
        card.setUpdateDate(Timestamp.valueOf(LocalDateTime.now()));
        card.setStatusCode('A');
        return card;
    }

