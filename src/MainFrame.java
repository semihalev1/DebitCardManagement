    private boolean addMainAccount(long customerId, String cardId, short branchCode) {
        int row = tbl_ekle_ana.getSelectedRow();
        long accountNo = Long.parseLong(tbl_ekle_ana.getValueAt(row, 1).toString());

        if (CompanyDbMenagement.doesMainAccountExist(customerId)) {
            showWarning("Bu müşterinin zaten bir ana hesabı var.");
            return false;
        }

        DebitCardAccounts account = CompanyDbMenagement.GetAccountById(accountNo);
        account.setCardUniqId(cardId);
        account.setBranchCode(branchCode);
        account.setAccountType('A');
        account.setUpdateDate(Timestamp.valueOf(LocalDateTime.now()));

        boolean isAdded = CompanyDbMenagement.AddAccount(account);
        if (!isAdded) {
            showWarning("Ana hesap eklenemedi.");
        }
        return isAdded;
    }

    private void addVirmanAccounts(String cardId, short branchCode) {
        int[] selectedRows = tbl_ekle_virman.getSelectedRows();
        if (selectedRows.length == 0) {
            return;
        }

        for (int i : selectedRows) {
            try {
                long accountNo = Long.parseLong(tbl_ekle_virman.getValueAt(i, 1).toString());
                DebitCardAccounts account = CompanyDbMenagement.GetAccountById(accountNo);
                account.setCardUniqId(cardId);
                account.setBranchCode(branchCode);
                account.setAccountType('V');
                account.setUpdateDate(Timestamp.valueOf(LocalDateTime.now()));

                if (!CompanyDbMenagement.AddAccount(account)) {
                    showWarning("Virman hesap " + accountNo + " eklenemedi.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Hesap işlenirken hata oluştu:\n" + e.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
            }
        }

        JOptionPane.showMessageDialog(this, "Virman hesap(lar) başarıyla eklendi.", "Bilgi", JOptionPane.INFORMATION_MESSAGE);
    }

