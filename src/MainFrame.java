    private void btn_update_cardActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_update_cardActionPerformed
        String cardNo = txt_card_id_update.getText();
        String cardId = CompanyDbMenagement.getCardIdByCardNo(cardNo);
        char status;
        int index = cb_status_card_update.getSelectedIndex();
        if (index == 0) {
            status = 'A';
        } else {
            status = 'K';
        }
        String dateText = txt_expiry_date.getText().trim();

        if (!isValidDateFormat(dateText)) {
            JOptionPane.showMessageDialog(this,
                    "Please enter the date in the format YYYY-MM-DD!",
                    "Invalid Date Format",
                    JOptionPane.WARNING_MESSAGE);
            return; // işlemi durdur
        }

// doğruysa devam et
        java.sql.Date expiryDate = java.sql.Date.valueOf(dateText);
        DebitCard card = new DebitCard();
        card = CompanyDbMenagement.GetCardByCardNo(cardNo);
        card.setExpiryDate(expiryDate);
        card.setStatusCode(status);
        boolean isUpdatedCard = false;
        //boolean isUpdatedAccountStatus = false;
        //boolean isUpdatedAuthStatus = false;
        try {
            isUpdatedCard = CompanyDbMenagement.UpdateCard(card);
            //isUpdatedAccountStatus = CompanyDbMenagement.UpdateAccountStatus(cardId, status);
            //isUpdatedAuthStatus = CompanyDbMenagement.UpdateDebitCardStatusAuthors(cardId, status);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString(), "uyarı", JOptionPane.WARNING_MESSAGE);
        }

        if (isUpdatedCard) {
            JOptionPane.showMessageDialog(this, "Kart Güncellendi", "Bilgi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Kart Güncellenemedi", "Uyarı", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_update_cardActionPerformed

