        short homeAddress = 1;
        short workAddress = 2;
        long musteriId = CompanyDbMenagement.getCustomerIdByCardNo(cardNo);

        DebitCardAddress evAdresi = new DebitCardAddress();
        DebitCardAddress isAdresi = new DebitCardAddress();
