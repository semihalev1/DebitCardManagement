    private void btn_degistirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_degistirActionPerformed
        String cardNo = txt_card_id_update.getText().trim();
        if (cardNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kart numarası boş olamaz!", "Uyarı", JOptionPane.WARNING_MESSAGE);
            return;
        }

        long musteriId = CompanyDbMenagement.getCustomerIdByCardNo(cardNo);
        short branchCode = CompanyDbMenagement.getBranchCodeByCardNo(cardNo);
        String cardId = CompanyDbMenagement.getCardIdByCardNo(cardNo);

        HesapSecEkrani secimEkrani = new HesapSecEkrani(this, true, musteriId, cardId, 'A');
        secimEkrani.setVisible(true);

        List<Object[]> secilenHesaplar = secimEkrani.getSecilenHesaplar();

        if (secilenHesaplar != null && !secilenHesaplar.isEmpty()) {
            // Önce tabloyu temizle
            mdl_güncelle_ana.setRowCount(0);

            // Eski DB kayıtlarını sil
            CompanyDbMenagement.deleteAllAccountsByCardIdAndType(cardId, 'A');

            for (Object[] hesap : secilenHesaplar) {
                // JTable'a satırı ekle
                mdl_güncelle_ana.addRow(hesap);

                try {
                    long hesapNo = Long.parseLong(hesap[1].toString());

                    DebitCardAccounts account = CompanyDbMenagement.GetAccountById(hesapNo);
                    account.setAccountType('A');
                    account.setBranchCode(branchCode);
                    account.setCardUniqId(cardId);
                    CompanyDbMenagement.AddAccount(account);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Hesap eklenirken hata oluştu:\n" + ex.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
                }
            }

            JOptionPane.showMessageDialog(this, "Seçilen hesap(lar) başarıyla eklendi.", "Bilgi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            System.out.println("Seçim yapılmadan pencere kapatıldı.");
        }
    }//GEN-LAST:event_btn_degistirActionPerformed

