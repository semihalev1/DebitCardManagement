        List<List<String>> accounts = CompanyDbMenagement.getAccountDetailsMainByCardNo(cardId);
        mdl_güncelle_ana.setRowCount(0);

        for (List<String> account : accounts) {
            String currency = account.get(0);
            String accountNo = account.get(1);
            String suffix = account.get(2);

            // Sadece mainAccounts içinde varsa ekle
            boolean isMatched = mainAccounts.stream()
                    .anyMatch(main -> main.get(0).equals(accountNo));

            if (isMatched) {
                Object[] rowData = {currency, accountNo, suffix};
                mdl_güncelle_ana.addRow(rowData);
            }
        }

        // VİRMAN HESAPLAR
        List<List<String>> virmanAccounts = CompanyDbMenagement.getAccountNumbersMainByCardNo(cardId, 'V');
        List<List<String>> accounts2 = CompanyDbMenagement.getAccountDetailsByCardNo(cardId);
        mdl_güncelle_virman.setRowCount(0);

        for (List<String> account : accounts2) {
            String currency = account.get(0);
            String accountNo = account.get(1);
            String suffix = account.get(2);

            // Sadece virmanAccounts içinde varsa ekle
            boolean isMatched = virmanAccounts.stream()
                    .anyMatch(virm -> virm.get(0).equals(accountNo));

            if (isMatched) {
                Object[] rowData = {currency, accountNo, suffix};
                mdl_güncelle_virman.addRow(rowData);
            }
        }

